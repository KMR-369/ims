package week1assignments;
/*
 * Program to print the Default values
 * of all Primitive Datatypes
 */
public class PrimitiveDefault {
	static int a;
	static long b;
	static float c;
	static double d;
	static boolean e;
	public static void main(String[] args) {
		 System.out.println("The default value of int datatype is:"+a);
		 System.out.println("The default value of long datatype is:"+b);
		 System.out.println("The default value of float datatype is:"+c);
		 System.out.println("The default value of double datatype is:"+d);
		 System.out.println("The default value of boolean datatype is:"+e);

	}

}
